                                <li><b>Гарантийная.</b> Доказательство высокого качества продукции. Потребители предпочитают товары с логотипом.</li>
                                <li><b>Эстетическая.</b> Логотип — элемент дизайна непосредственно товара или его упаковки.</li>
                                <li><b>Рекламная.</b> Формирование общественного мнения о компании. Предоставление информации и влияние на психологию потребителя.</li>
                            </ul>
                            Создание логотипа в СПб или Москве позволяет решить те же проблемы, как и разработка фирменного стиля. Поэтому нужно тщательно выбирать разработчика.
